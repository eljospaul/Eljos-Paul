const root = document.querySelector(":root")
const icon = document.getElementById("modeIconBtn")
const icon2 = document.getElementById("modeIconBtn2")
if (!localStorage.getItem("mode")) {
    localStorage.setItem("mode", "light")
}
const webMode = localStorage.getItem("mode")
if (webMode === "light") {
    light()
} else if (webMode === "dark") {
    dark()
}
function mode() {
    const get = getComputedStyle(root).getPropertyValue("--body")
    if (get === " #fff") {
        localStorage.setItem("mode", "dark");
        dark()
    } else if (get === "#202124") {
        localStorage.setItem("mode", "light")
        light()
    } else {
        localStorage.setItem("mode", "dark")
        dark()
    }
}
